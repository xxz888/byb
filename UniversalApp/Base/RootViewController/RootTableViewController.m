#pragma mark - 键盘处理
#pragma mark 监听系统发出的键盘通知
- (void)addKeyboardNote{
    [[NSNotificationCenter defaultCenter] addObserver:self
                                             selector:@selector(keyboardWillShow:)
                                                 name:UIKeyboardWillShowNotification
                                               object:nil];
    
    //监听当键将要退出时
    [[NSNotificationCenter defaultCenter] addObserver:self
                                             selector:@selector(keyboardWillHide:)
                                                 name:UIKeyboardWillHideNotification
                                               object:nil];
    
}
#pragma mark 显示一个新的键盘就会调用
- (void)keyboardWillShow:(NSNotification *)notification{
    //    CGFloat h = self.btnRegister.frame.origin.y +self.btnRegister.frame.size.height;
    //获取键盘的高度
    NSDictionary *userInfo = [notification userInfo];
    NSValue *value = [userInfo objectForKey:UIKeyboardFrameEndUserInfoKey];
    CGRect keyboardRect = [value CGRectValue];
    CGFloat keyboardH = keyboardRect.size.height;
    self.view.transform = CGAffineTransformMakeTranslation(0,  - keyboardH );
}
#pragma mark 隐藏键盘就会调用
- (void)keyboardWillHide:(NSNotification *)notification{
    self.view.transform = CGAffineTransformIdentity;
}
- (void)touchesBegan:(NSSet *)touches withEvent:(UIEvent *)event{
    [[[UIApplication sharedApplication] keyWindow] endEditing:YES];
}

